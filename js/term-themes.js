function applyTermThemeVars(body, theme) {
  var c = theme.colors;
  body.style.setProperty('--t-bg1', c.bg1);
  body.style.setProperty('--t-bg2', c.bg2);
  body.style.setProperty('--t-bg3', c.bg3);
  body.style.setProperty('--t-accent', c.accent);
  body.style.setProperty('--t-warm', c.warm || c.bg3);
  body.style.setProperty('--t-gold', c.gold || '#d2b450');
  body.style.setProperty('--t-ivory', c.ivory || '#f5f2eb');
  body.style.setProperty('--t-ink', c.ink || '#5a464b');
  body.style.setProperty('--t-ink-soft', c.inkSoft || '#827068');
}
